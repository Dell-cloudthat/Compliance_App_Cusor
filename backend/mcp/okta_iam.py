def call_tool(tool_name: str, args: Dict[str, Any], client: OktaIAMClient) -> Any:
    """Dispatch an MCP tool call to the correct OktaIAMClient method."""
    dispatch = {
        "okta_list_users":       lambda: client.list_users(**args),
        "okta_get_user_count":   lambda: client.get_user_count(),
        "okta_list_groups":      lambda: client.list_groups(**args),
        "okta_list_apps":        lambda: client.list_apps(**args),
        "okta_get_user_app_access": lambda: client.get_user_app_access(args["user_id"]),
        "okta_inactive_users":   lambda: client.inactive_users(**args),
        "okta_mfa_adoption":     lambda: client.mfa_adoption(),
    }
    fn = dispatch.get(tool_name)
    if fn is None:
        raise ValueError(f"Unknown tool: {tool_name}")
    return fn()


