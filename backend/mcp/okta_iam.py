if __name__ == "__main__":
    import argparse, sys

    parser = argparse.ArgumentParser(description="Okta IAM MCP Server")
    parser.add_argument("--domain", required=True, help="Okta domain, e.g. acme.okta.com")
    parser.add_argument("--token",  required=True, help="Read-only Okta API token")
    args = parser.parse_args()

    client = OktaIAMClient(domain=args.domain, token=args.token)

    # Simple stdio JSON-RPC loop (compatible with MCP Inspector)
    print(json.dumps({"jsonrpc": "2.0", "method": "tools/list", "result": {"tools": MCP_TOOLS}}))
    sys.stdout.flush()

    for line in sys.stdin:
        try:
            req = json.loads(line)
            tool = req.get("params", {}).get("name")
            tool_args = req.get("params", {}).get("arguments", {})
            result = call_tool(tool, tool_args, client)
            print(json.dumps({"jsonrpc": "2.0", "id": req.get("id"), "result": {"content": [{"type": "text", "text": json.dumps(result)}]}}))
        except Exception as e:
            print(json.dumps({"jsonrpc": "2.0", "id": None, "error": {"code": -32000, "message": str(e)}}))
        sys.stdout.flush()


